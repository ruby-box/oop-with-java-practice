package tdd.calculator;

public class Calculator {
    public int sum(int i, int i1) {
        return i + i1;
    }

    public double minus(int i, int i1) {
        return i - i1;
    }

    public double multiple(int i, int i1) {
        return i * i1;
    }

    public double divide(int i, int i1) {
        return i / i1;
    }
}
